public class Asignacion {
  public static void main(String[] args) {
    int x = 10; // igualdad

    x += 5;  System.out.println(x); // suma igual
    x -= 3;  System.out.println(x); // resta igual
    x *= 2;  System.out.println(x); // multiplicación igual
    x /= 4;  System.out.println(x); // división igual
    x %= 4;  System.out.println(x); // modulo igual
  }
}