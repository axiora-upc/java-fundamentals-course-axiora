public class Relacionales {
  public static void main(String[] args) {
    int a = 8, b = 5;

    System.out.println(a == b); // false → igual a
    System.out.println(a != b); // true  → diferente de
    System.out.println(a >  b); // true  → mayor que
    System.out.println(a <  b); // false → menor que
    System.out.println(a >= b); // true  → mayor o igual
    System.out.println(a <= b); // false → menor o igual
  }
}