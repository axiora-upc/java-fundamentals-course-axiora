public class Arrays {

    public static void main(String[] args) {
        String[] estudiantes = { "Ana", "Luis", "Carlos" };

        System.out.println(estudiantes[0]);

        estudiantes[1] = "Pedro";

        System.out.println(estudiantes[1]);

        int[] numeros = { 1, 2, 3, 4, 3, 1 };
        System.out.println(numeros[0]);
    }
}
